# 1 TO 10

import time

countdown = 1
while countdown <= 10:
    print(f"{countdown}...")
    time.sleep(1)  
    countdown += 1

print(" Happy New Year!!!")



# Reverse Method  10 TO 1
import time

countdowns = 10
time.sleep(2)
while countdowns >= 1:
    print(f"{countdowns}...")
    countdowns -= 1
print("Happy New Year!!!")